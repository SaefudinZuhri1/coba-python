# Gabung Tuple

# A. Gabungkan Dua Tuple
# Untuk menggabungkan dua tuple atau lebih, bisa menggunakan operator +
# Contoh menggabungkan dua tuple:
iniHuruf = ("a", "b", "c")
iniAngka = (1, 2, 3)

gabungan = iniHuruf + iniAngka
print(gabungan)

# Melipatkgandakan Tuple
# Jika ingin mengalikan konten tuple beberapa kali, dapat menggunakan * operator
# Contoh: Menggandakan tuple dengan 4
hewan = ("Ayam", "Anjing", "Unta")
gandakan = hewan * 4
print(gandakan)
